#!/bin/bash

USER_ID=$(id -u)
if [ "${USER_ID}" -ne 0 ]; then
  echo -e "\e[1;31mYou should be a root user to perform this command\e[0m"
  exit 1
fi

PRINT() {
  echo "------------------------------------------------------------------------------------------------------------"
  echo -e "\e[1;35m [INFO] $1 \e[0m"
  echo "------------------------------------------------------------------------------------------------------------"
}


if [ $? -ne 0 ]; then
  echo "------------------------------------------------------------------------------------------------------------"
  echo -e "\e[1;31m [ERROR] Nginx Installation is failure\e[0m"
  exit 2
else
  echo "------------------------------------------------------------------------------------------------------------"
  echo -e "\e[1;32m [SUCC] Nginx Installation is successful"
fi

echo "------------------------------------------------------------------------------------------------------------"
